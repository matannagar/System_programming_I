#include "myMath.h"

float add(float x,float y)
{
    float sum =0;

    sum = x+y;

    return sum;
}

float sub(float x,float y)
{
    float subtraction = 0;
    subtraction = x-y;

    return subtraction;
}

double mul(double x, int y)
{
    double mul = 0;
    mul = x*(double)y;
    return mul;
}

double divi(double x,int y)
{
double division;
division = x/y;

return (double)division;
}
